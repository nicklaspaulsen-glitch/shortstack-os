import type { ContentIdea, SocialPlatform, TrendsResponse } from "@/lib/social-studio/types";

/**
 * GET /api/social/trends?niche=<niche>
 *
 * Returns:
 *  - hashtags_by_platform: top hashtags per platform (Zernio when
 *    available, fallback dictionary otherwise — all keyed by the niche
 *    string).
 *  - ideas: 10 short post ideas for the next ~7 days, generated by
 *    Claude Haiku. Refresh button on the UI hits the same route.
 *
 * v1 doesn't yet hit a Zernio "trending hashtags" endpoint — Zernio
 * doesn't expose that publicly. Once we ship a webhook that mirrors
 * trending tags into a `social_trending_hashtags` table this route
 * will read from there. For now we serve the curated FALLBACK_HASHTAGS
 * augmented with niche-aware tags from Claude.
 */
interface ClaudeTrendsResponse {
  ideas: ContentIdea[];
  niche_hashtags?: Partial<Record<SocialPlatform, string[]>>;
}

const SYSTEM_PROMPT = `You are a social media trends researcher. The user gives you a niche (e.g. "digital marketing agency", "fitness coach", "ecommerce founder") and you return:
1. 10 concrete post ideas for the next ~7 days. Each idea has: title (under 70 chars), hook (under 140 chars), format (one of: reel, carousel, single-image, thread, short-form-video, long-form-video, text-post), platforms (subset of: instagram, tiktok, linkedin, facebook, twitter, youtube, pinterest, threads).
2. Up to 8 niche-specific hashtags per platform that complement generic ones.

Mix evergreen + timely. Avoid generic clickbait. STRICT JSON output, no prose, no markdown fences:
{
  "ideas": [
    { "title": "...", "hook": "...", "format": "...", "platforms": ["..."] }
  ],
  "niche_hashtags": {
    "instagram": ["#tag", ...],
    "linkedin": ["#tag", ...]
  }
}`;

function mergeHashtags(
  fallback: Record<SocialPlatform, string[]>,
  niche: Partial<Record<SocialPlatform, string[]>> | undefined,
): Partial<Record<SocialPlatform, string[]>> {
  const merged: Partial<Record<SocialPlatform, string[]>> = {};
  for (const platform of ALL_PLATFORMS) {
    const generic = fallback[platform];
    const nicheTags = niche?.[platform] ?? [];
    const seen = new Set<string>();
    const combined: string[] = [];
    for (const tag of [...nicheTags, ...generic]) {
      const normalised = tag.startsWith("#") ? tag : `#${tag}`;
      const key = normalised.toLowerCase();
      if (!seen.has(key) && normalised.length > 1) {
        seen.add(key);
        combined.push(normalised);
        if (combined.length >= 15) break;
      }
    }
    merged[platform] = combined;
  }
  return merged;
}

