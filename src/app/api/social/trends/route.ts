export async function GET(request: NextRequest) {
  const supabase = createServerSupabase();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  // Rate limit AI usage.
  const { data: profile } = await supabase
    .from("profiles")
    .select("plan_tier")
    .eq("id", user.id)
    .single();
  const limited = checkAiRateLimit(user.id, profile?.plan_tier);
  if (limited) return limited;

  const niche = (new URL(request.url).searchParams.get("niche") ?? "digital marketing agency").slice(0, 200);

  let ideas: ContentIdea[] = DEFAULT_IDEAS;
  let nicheHashtags: Partial<Record<SocialPlatform, string[]>> = {};

  try {
    const response = await callLLM({
      taskType: "generation_short",
      systemPrompt: SYSTEM_PROMPT,
      userPrompt: `Niche: ${niche}`,
      maxTokens: 2200,
      userId: user.id,
      context: "/api/social/trends",
    });
    const parsed = safeJsonParse<ClaudeTrendsResponse>(response.text);
    if (parsed) {
      if (Array.isArray(parsed.ideas) && parsed.ideas.length > 0) {
        ideas = parsed.ideas
          .filter((i) => i && typeof i.title === "string")
          .slice(0, 10)
          .map((i) => ({
            title: String(i.title).slice(0, 140),
            hook: String(i.hook ?? "").slice(0, 240),
            format: String(i.format ?? "single-image"),
            platforms: Array.isArray(i.platforms)
              ? (i.platforms.filter((p) => ALL_PLATFORMS.includes(p as SocialPlatform)) as SocialPlatform[])
              : [],
          }));
      }
      if (parsed.niche_hashtags) nicheHashtags = parsed.niche_hashtags;
    }
  } catch (err) {
    console.error("[social-studio/trends] claude error", err);
    // Fall through to defaults.
  }

  const merged = mergeHashtags(FALLBACK_HASHTAGS, nicheHashtags);

  const out: TrendsResponse = {
    hashtags_by_platform: merged,
    ideas,
    niche,
  };

  return NextResponse.json(out);
}
