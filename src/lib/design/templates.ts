/**
 * Deep-clone a template doc, replacing all layer ids and the page id
 * with fresh UUIDs so the new design is fully independent.
 */
export function cloneTemplateDoc(doc: DesignDoc): DesignDoc {
  const cloned: DesignDoc = JSON.parse(JSON.stringify(doc)) as DesignDoc;
  cloned.pages = cloned.pages.map((page) => {
    const idMap: Record<string, string> = {};
    const newPage = { ...page, id: uuidv4() };
    newPage.layerIds = page.layerIds.map((oldId) => {
      const newId = uuidv4();
      idMap[oldId] = newId;
      return newId;
    });
    const newLayers: typeof page.layers = {};
    for (const [oldId, layer] of Object.entries(page.layers)) {
      const newId = idMap[oldId] ?? uuidv4();
      newLayers[newId] = { ...layer, id: newId };
    }
    newPage.layers = newLayers;
    return newPage;
  });
  return cloned;
}
