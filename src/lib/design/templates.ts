import { v4 as uuidv4 } from "uuid";
import type { DesignDoc, Page, TextLayer, ShapeLayer, ImageLayer } from "./types";

function makeId() {
  return uuidv4();
}

function makePage(
  bg: string,
  layers: Array<TextLayer | ShapeLayer | ImageLayer>,
): Page {
  const layerMap: Record<string, TextLayer | ShapeLayer | ImageLayer> = {};
  const layerIds: string[] = [];
  layers.forEach((l, i) => {
    layerMap[l.id] = { ...l, zIndex: i };
    layerIds.push(l.id);
  });
  return {
    id: makeId(),
    name: "Page 1",
    backgroundColor: bg,
    layerIds,
    layers: layerMap,
  };
}

