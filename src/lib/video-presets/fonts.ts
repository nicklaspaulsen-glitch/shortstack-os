/** Filter fonts by category. */
export function filterFontsByCategory(
  category: FontCategory,
): Font[] {
  return FONTS_LIBRARY.filter((f) => f.category === category);
}

/** Return fonts whose useCases include any of the requested tags. */
export function filterFontsByUseCase(tags: string[]): Font[] {
  if (!tags.length) return FONTS_LIBRARY;
  const s = new Set(tags.map((t) => t.toLowerCase()));
  return FONTS_LIBRARY.filter((f) =>
    f.useCases.some((u) => s.has(u.toLowerCase())),
  );
}

/** Return a font by id, or undefined. */
export function getFontById(id: string): Font | undefined {
  return FONTS_LIBRARY.find((f) => f.id === id);
}

/** Pseudo-random font pick (deterministic when a seed is passed). */
export function getRandomFont(seed?: number): Font {
  const idx =
    typeof seed === "number"
      ? Math.abs(Math.floor(seed)) % FONTS_LIBRARY.length
      : Math.floor(Math.random() * FONTS_LIBRARY.length);
  return FONTS_LIBRARY[idx]!;
}
