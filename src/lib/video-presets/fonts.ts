/**
 * FONTS_LIBRARY — 100+ web fonts for the video editor & AI tool pages.
 *
 * All URLs are real Google Fonts CSS2 endpoints (https://fonts.googleapis.com/css2).
 * They're loadable in-browser via <link rel="stylesheet"> or programmatic import.
 * The `family` field matches the exact CSS font-family name after loading.
 *
 * Categories are coarse grained — a font can belong to multiple aesthetic buckets
 * (e.g. "Cinzel" is both serif and gothic); we pick the dominant one here and use
 * the `useCases` tags for secondary discovery.
 */

export type FontCategory =
  | "display"
  | "sans"
  | "serif"
  | "mono"
  | "handwriting"
  | "gothic";

export interface Font {
  id: string;
  family: string;
  url: string;
  category: FontCategory;
  /** Canonical weights bundled in the URL. 400 = regular, 700 = bold, 900 = black. */
  weight: number[];
  /** Discovery tags: "ad-headline", "caption", "body", "quote", "metal", etc. */
  useCases: string[];
  previewText: string;
}

// Helper to build Google Fonts CSS2 URLs with a single weight.
const gf = (family: string, weights: number[] = [400, 700]): string => {
  const qFamily = family.replace(/ /g, "+");
  const wts = weights.join(";");
  return `https://fonts.googleapis.com/css2?family=${qFamily}:wght@${wts}&display=swap`;
};

